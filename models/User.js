class User {
   constructor(
      id,
      email,
      name,
      phone,
      city,
      address,
      postalCode,
      birthDate,
      accountNumber
   ) {
      this.id = id;
      this.email = email;
      this.name = name;
      this.phone = phone;
      this.city = city;
      this.address = address;
      this.postalCode = postalCode;
      this.birthDate = birthDate;
      this.accountNumber = accountNumber;
   }
}
export default User;
